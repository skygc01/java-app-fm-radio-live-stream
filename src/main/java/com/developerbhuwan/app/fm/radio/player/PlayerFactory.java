package com.developerbhuwan.app.fm.radio.player;

/**
 *
 * @author Bhuwan Prasad Upadhyay
 */
public class PlayerFactory {

    public static FmPlayer getPlayer(PlayerDTO player) {
        if (player == null || player.getUrl() == null) {
            throw new IllegalArgumentException("Empty player");
        }
        if (player.getUrl().toLowerCase().startsWith("http:")) {
            return new HttpUrlPlayer(player);
        } else if (player.getUrl().toLowerCase().startsWith("https")) {
            return new HttpsUrlPlayer(player);
        }
        throw new IllegalArgumentException("Player not found! " + player);
    }
}
